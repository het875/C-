#include <iostream>
using namespace std;
int main()
{
    int a, b;

    cout << "Hello World\n";
    cout << "Enter A: ";
    cin  >> a;
    cout << "Enter B: ";
    cin  >> b;

    cout << "Value of A: " <<a << "\nValue of B: " <<b; 

    cout << "\nValue of A+B: " <<a+b;

    cout << "\nValue of A-B: " <<a-b;

    cout << "\nValue of A*B: " <<a*b;

    cout << "\nValue of A/B: " <<a/b;

    cout << "\nValue of A%B: " <<a%b;
}